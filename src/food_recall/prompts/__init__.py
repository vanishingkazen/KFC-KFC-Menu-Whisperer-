def get_prompt(key: str, **kwargs) -> str:
    """
    获取 Prompt 模板并填充变量

    Args:
        key: Prompt 键名
        **kwargs: 模板变量

    Returns:
        填充后的 Prompt
    """
    template = PROMPTS.get(key)
    if not template:
        raise ValueError(f"Prompt key '{key}' not found")
    return template.format(**kwargs)


def list_prompts() -> list[str]:
    """列出所有可用的 Prompt 键"""
    return list(PROMPTS.keys())